    // Fungsi rekrusif untuk mencetak nilai
    private static int angka(int nilai){    
        if(nilai==0){
            nilai+=75;
            return nilai;
        }
        else{
            return angka(nilai-1);
        }
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here
        String keterangan="";
        String nama = textboxNama.getText();
        int nilai = Integer.parseInt(textboxNilai.getText());
        
        if(nilai < angka(nilai)){
            keterangan="Tidak Lulus";
        }
        else{
            keterangan="Lulus";
        }
        
        DefaultTableModel dataModel = (DefaultTableModel) tabel.getModel();
        List list = new ArrayList<>();
        tabel.setAutoCreateColumnsFromModel(true);
        list.add(nama);
        list.add(nilai);
        list.add(keterangan);
        dataModel.addRow(list.toArray());     
    }//GEN-LAST:event_btnSaveActionPerformed

